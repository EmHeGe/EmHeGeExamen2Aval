#!/bin/bash
contador=0
consumos=()
premio=''

while read -r line
do
    if [ $contador -gt 0 ]
    then
        ciudad=$(echo "$line" | cut -d ' ' -f 1)
        mes=$(echo "$line" | cut -d ' ' -f 2)
        consumo=$(echo "$line" | cut -d ' ' -f 4)

        # Add consumption value to the array for the corresponding month
        case $mes in
            "Enero")   consumos[0]+=$consumo;;
            "Febrero") consumos[1]+=$consumo;;
            "Marzo")   consumos[2]+=$consumo;;
            "Abril")   consumos[3]+=$consumo;;
            "Mayo")    consumos[4]+=$consumo;;
            "Junio")   consumos[5]+=$consumo;;
            "Julio")   consumos[6]+=$consumo;;
            "Agosto")  consumos[7]+=$consumo;;
            "Septiembre") consumos[8]+=$consumo;;
            "Octubre") consumos[9]+=$consumo;;
            "Noviembre") consumos[10]+=$consumo;;
            "Diciembre") consumos[11]+=$consumo;;
        esac
    fi
    ((contador++))
done < consumos.txt

